namespace TurboLibrary
{
    /// <summary>
    /// Represents the outer forms an area or clip area spans.
    /// </summary>
    public enum AreaShape
    {
        /// <summary>
        /// The area spans a cuboid region.
        /// </summary>
        Cube = 0,

        /// <summary>
        /// The area spans a cylindrical region.
        /// </summary>
        Cylinder = 1
    }
}
